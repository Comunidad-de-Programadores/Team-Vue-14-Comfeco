const router = new VueRouter({
  mode: 'history',
  base: process.env.BASE_URL,
  routes
})

router.beforeEach((to, from, next) => {
  const token = localStorage.getItem('token') || null
  if (to.meta.requiresAuth) {
    if (token === null || token === '') {
      next({
        name: 'Login'
      });
    } else {
      next();
    }
  } else {
    next();
  }
})

export default router
